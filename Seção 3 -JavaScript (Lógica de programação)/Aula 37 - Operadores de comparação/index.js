/*

Operadores de comparação

<   Menor
<=  Menor que

>   Maior
>=  Maior que

==  Igualdade
=== Igualdade estrita

!=  Diferente
!== Diferente estrito

*/

const comp = 10 > 5;
console.log(comp);

const comp1 = 10 >= 5;
console.log(comp1);

const comp2 = 10 === 11; // 10 é igual a 11? false
console.log(comp2);

const comp3 = 10 !== 11; 
console.log(comp3);



